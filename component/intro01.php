<h3>인사말</h3>

<div class="">
이어지는 내용은 정하상 교육관과 프란치스코교육관의 인사말을 짜집기 한 것입니다. 양지 IC에서 2Km(10분)거리에 위차하고 있는 저희 영성교육관은 2020년 개관하여 피정, 연수, 교육을 하기에 좋은 시설과 환경을 갖추고 있습니다.
최대 00명을 수용 할 수 있는 숙소동, 잘 꾸며진 잔디구장, 조용히 기도할 수 있는 성당과 경당은 이곳에 오는 이들에게 좋은 추억으로 남을 것입니다.
평일미사,고해성사,음악 공연 등 알차고 친근한 교육, 문화프로그램을 진행하는 영성교육관은 모두에게 열려있고 피정과 교육을 원하는 모든 개인과단체를 초대합니다.

